#include <iostream>
#include <cmath> // Include cmath library for mathematical functions
using namespace std;

int main() {
    cout << sqrt(4) << endl;    // Prints square root of 4
    cout << cbrt(64) << endl;   // Prints cube root of 64
    cout << pow(4, 3) << endl;  // Prints 4 raised to the power of 3
    cout << min(4, 10) << endl; // Prints the smaller of 4 and 10
    cout << max(4, 10) << endl; // Prints the larger of 4 and 10
    return 0;
}
