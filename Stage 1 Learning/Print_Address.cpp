#include <iostream>
using namespace std;

int main() {
    int x = 3, y = 5;

    // Printing address and value of variables
    cout << "The address of x is " << &x << " and the value is " << x << endl;
    cout << "The address of y is " << &y << " and the value is " << y << endl;

    return 0;
}
